    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        // code to load data into table
        if(txtName.getText().equals("")||txtSalary.getText().equals("")||txtCarModel.getText().equals("")){
            //if any of the three field left blank, show message
            JOptionPane.showMessageDialog(this, "Please Enter All Data");
        }else{
            //if all data field is filled
            //store enter data into String array
            String data[]={txtName.getText(),txtSalary.getText(),txtCarModel.getText()};
            
            DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
            //add String array data
            tblModel.addRow(data); //row added
            //successfully added message
            JOptionPane.showMessageDialog(this, "Add Data Successfully!");
            //clear textfield for new entry
            txtName.setText("");
            txtSalary.setText("");
            txtCarModel.setText("");
        }
    }//GEN-LAST:event_btnAddActionPerformed

