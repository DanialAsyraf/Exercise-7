    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
        
        //delete row
        if(jTable1.getSelectedRowCount()==1){
            //if single row is selected, delete
            tblModel.removeRow(jTable1.getSelectedRow());
            
        }else{
            if(jTable1.getRowCount()==0){
                //if table is empty,display message
                JOptionPane.showMessageDialog(this, "Table is Empty!");
            }else{
                //if table is not empty but row is not selected,or multiple row is selected
                JOptionPane.showMessageDialog(this, "Please select Single Row to be Deleted.");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        //set data to their textfield
        DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
        
        //set data to textfield when row is selected
        String tblName = tblModel.getValueAt(jTable1.getSelectedRow(), 0).toString();
        String tblSalary = tblModel.getValueAt(jTable1.getSelectedRow(), 1).toString();
        String tblCarModel = tblModel.getValueAt(jTable1.getSelectedRow(), 2).toString();
        
        //set to textfield
        txtName.setText(tblName);
        txtSalary.setText(tblSalary);
        txtCarModel.setText(tblCarModel);
        
    }//GEN-LAST:event_jTable1MouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        //get table
        DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
        if(jTable1.getSelectedRowCount()==1){
            //if single row is selected,update
            String name = txtName.getText();
            String salary = txtSalary.getText();
            String carModel = txtCarModel.getText();
            //set updated value in table row
            tblModel.setValueAt(name, jTable1.getSelectedRow(), 0);
            tblModel.setValueAt(salary, jTable1.getSelectedRow(), 1);
            tblModel.setValueAt(carModel, jTable1.getSelectedRow(), 2);
            //update message display
            JOptionPane.showMessageDialog(this, "Update Successfully!");
            
        }else{
            if(jTable1.getRowCount()==0){
                //if table is empty
                JOptionPane.showMessageDialog(this, "Table is Empty.");
            }else{
                //if row is not selected
                JOptionPane.showMessageDialog(this, "Please select single row to be updated.");
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed
